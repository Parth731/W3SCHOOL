
// while loop
// syntax
/*
while (condition) {
  // code block to be executed
}
*/
let i=0;
text = "";
while(i<10){
    text += i + " ";
    i++;
}
console.log(i);


// do-while loop
// syntax
/*
do {
  // code block to be executed
}
while (condition);
*/

text = "";
i = 0;
do{
    text += i + " ";
    i++;
}while(i<10);
console.log(i);

